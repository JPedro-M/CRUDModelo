    public void showTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbModelos.getModel();
        modelo.setNumRows(0);
        ModeloDao mdao = new ModeloDao();
        for (Modelo m : mdao.read()) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getModelo(),
                m.getMarca(),
                m.getAno()
            });
        }
    }

    public void showTableForName(String modelo) throws SQLException {
        DefaultTableModel model = (DefaultTableModel) tbModelos.getModel();
        model.setNumRows(0);
        ModeloDao mdao = new ModeloDao();
        for (Modelo m : mdao.readPesq(modelo)) {
            model.addRow(new Object[]{
                m.getId(),
                m.getModelo(),
                m.getMarca(),
                m.getAno()
            });
        }
    }

