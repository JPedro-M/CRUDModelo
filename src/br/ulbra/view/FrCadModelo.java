    private void tbModelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbModelosMouseClicked
        if (tbModelos.getSelectedRow() != -1) {
            txtid.setText(tbModelos.getValueAt(tbModelos.getSelectedRow(), 0)
                    .toString());
            txtModelo.setText(tbModelos.getValueAt(tbModelos.getSelectedRow(), 1)
                    .toString());
            txtMarca.setText(tbModelos.getValueAt(tbModelos.getSelectedRow(), 2)
                    .toString());
            txtAno.setText(tbModelos.getValueAt(tbModelos.getSelectedRow(), 3)
                    .toString());
            btSalvar.setEnabled(false);
            btAlterar.setEnabled(true);
            btExcluir.setEnabled(true);

        }
    }//GEN-LAST:event_tbModelosMouseClicked

